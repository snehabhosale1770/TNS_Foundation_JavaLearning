public class Person {

    // Data Member
    private String name;
    private int income;
    private String gender;
    private int age;
    private int tax;

    public String getName() {
        return name;
    }

    public int getIncome() {
        return income;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }

    public int getTax() {
        return tax;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setIncome(int income) {
        this.income = income;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setTax(int tax) {
        this.tax = tax;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
